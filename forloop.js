// for loop in JavaScript
// for (initialization; condition; increment/decrement) {
//   // code to be executed
// }

for (let index = 0; index < 10; index++) {
  const element = index;
  console.log(`Index: ${element}`);
}

for (let i = 0; i < 10; i++) {
  console.log(`Index: ${i}`);
  if (i == 5) {
    console.log("5 is a lucky number!");
  }
}
